#include<iostream>
using namespace std;

int main()
{
    cout<<"The size of int :"<<sizeof(int) << " bytes"<<endl;
    cout<<"The size of float :"<<sizeof(float) << " bytes"<<endl;
    cout<<"The size of double :"<<sizeof(double) << " bytes"<<endl;
    cout<<"The size of character :"<<sizeof(char) << " bytes"<<endl;
    return 0;
}